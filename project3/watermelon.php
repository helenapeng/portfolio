<h2 id="sec3">Cultivation</h2>
	<p>Watermelons are plants grown from tropical to temperate climates, needing temperatures higher than about <strong>25 °C (77 °F)</strong> to thrive. On a garden scale, seeds are usually sown in pots under cover and transplanted into well-drained sandy loam with a pH between 5.5 and 7 and medium levels of nitrogen.</p>
			<p>Major pests of the watermelon include aphids, fruit flies, and root-knot nematodes. In conditions of high humidity, the plants are prone to plant diseases such as powdery mildew and mosaic virus. Some varieties often grown in Japan and other parts of the Far East are susceptible to fusarium wilt. Grafting such varieties onto disease-resistant rootstocks offers protection.</p>
			<figure>
				<a><img src="images/melon4.jpg" alt="Watermelon"></a>
				<figcaption>Seedless watermelon</figcaption>
			</figure>
			<p>The US Department of Agriculture recommends using at least one beehive per acre (4,000 m2 per hive) for pollination of conventional, seeded varieties for commercial plantings. Seedless hybrids have sterile pollen. This requires planting pollinizer rows of varieties with viable pollen. Since the supply of viable pollen is reduced, and pollination is much more critical in producing the seedless variety, the recommended number of hives per acre increases to three hives per acre (1,300 m2 per hive). Watermelons have a longer growing period than other melons and can often take 85 days or more from the time of transplanting for the fruit to mature. Lack of pollen is thought to contribute to "hollow heart" which causes the flesh of the watermelon to develop a large hole, sometimes in an intricate, symmetric shape. Watermelons suffering from hollow heart are safe to consume.</p>

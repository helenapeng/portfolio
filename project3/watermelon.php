	<section>
		<h2 id="sec4">History</h2>
			<p>Watermelons were originally cultivated for their high water content and were stored to be eaten during dry seasons, not only as a food source, but as a method of storing water. Watermelon seeds were found in the Dead Sea region at the ancient settlements of Bab edh-Dhra and Tel Arad. Many 5000-year-old wild watermelon seeds (<em>C. lanatus</em>) were discovered at Uan Muhuggiag, a prehistoric archaeological site located in southwestern Libya. This archaeobotanical discovery may support the possibility that the plant was more widely distributed in the past.</p>
			<figure>
				<a><img src="images/melon3.jpg" alt="Watermelon"></a>
				<figcaption><em>Still Life with Watermelons, Pineapple and Other Fruit</em></figcaption>
			</figure>
			<p>In the 7th century, watermelons were being cultivated in India, and by the 10th century had reached China. The Moors introduced the fruit into the Iberian Peninsula, and there is evidence of it being cultivated in Córdoba in 961 and also in Seville in 1158. It spread northwards through southern Europe, perhaps limited in its advance by summer temperatures being insufficient for good yields. The fruit had begun appearing in European herbals by 1600, and was widely planted in Europe in the 17th century as a minor garden crop.</p>
			<p>Early watermelons were not sweet, but bitter, with yellowish-white flesh and difficult to open. Through breeding, watermelons later tasted better and were easier to open.</p>
			<p>European colonists and enslaved people from Africa introduced the watermelon to the New World. Spanish settlers were growing it in Florida in 1576. It was being grown in Massachusetts by 1629, and by 1650 was being cultivated in Peru, Brazil and Panama. Around the same time, Native Americans were cultivating the crop in the Mississippi valley and Florida. Watermelons were rapidly accepted in Hawaii and other Pacific islands when they were introduced there by explorers such as Captain James Cook. In the Civil War era United States, watermelons were commonly grown by free black people and became one symbol for the abolition of slavery. After the Civil War, black people were maligned for their association with watermelon. The sentiment evolved into a racist stereotype where black people shared a supposed voracious appetite for watermelon, a fruit long correlated with laziness and uncleanliness.</p>
			<p>Seedless watermelons were initially developed in 1939 by Japanese scientists who were able to create seedless triploid hybrids which remained rare initially because they did not have sufficient disease resistance. Seedless watermelons became more popular in the 21st century, rising to nearly 85% of total watermelon sales in the United States in 2014.</p>
